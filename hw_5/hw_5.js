// –ï—Å–ª–∏ –¥–≤–∞ —á–∏—Å–ª–∞ —Ä–∞–≤–Ω—ã, –≤–µ—Ä–Ω–∏—Ç–µ a –∏–ª–∏ b.
/*
  ```
    getSum(1, 0) == 1   // 1 + 0 = 1
    getSum(1, 2) == 3   // 1 + 2 = 3
    getSum(0, 1) == 1   // 0 + 1 = 1
    getSum(1, 1) == 1   // 1 Since both are same
    getSum(-1, 0) == -1 // -1 + 0 = -1
    getSum(-1, 2) == 2  // -1 + 0 + 1 + 2 = 2
```
*/

function getSum(a, b) {
    if (a === b) {
        return a
    }
    const minNum = Math.min(a, b)
    const maxNum = Math.max(a, b)
    let sumAll = 0
    for (let i = minNum; i <= maxNum; i++) {
        sumAll += i
    }
    return sumAll
}

